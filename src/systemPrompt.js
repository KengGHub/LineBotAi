export function buildUserPrompt({ message, faqText }) {
  const faq = faqText?.trim()
    ? faqText.trim()
    : "ยังไม่ได้ตั้งค่า FAQ สินค้าไว้ในระบบ";

  return `
<faq>
${faq}
</faq>

<customer_message>
${message}
</customer_message>

ตอบลูกค้าตาม System Prompt และ FAQ เท่านั้น
`.trim();
}
